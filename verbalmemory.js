let seenWords = []

let randomWords = "";

function pickRandomWord() {
randomWords = arrOfWords[Math.floor(Math.random() * arrOfWords.length)];
randomWord.innerText = randomWords;
}


startBtn.addEventListener('click', () => {
    introDiv.style.display = "none";
    seenDiv.style.display = "flex";
    pickRandomWord()
});

seenBtn.addEventListener('click', () => {
    if (seenWords.includes(randomWords)) {
        pickRandomWord();
        scoreCounter++;
        scoreText.innerText = "Score | " + scoreCounter;
    } else {
        pickRandomWord();
        liveCounter--;
        livesText.innerText = "Lives | " + liveCounter;
        if (liveCounter == 0) {
            seenDiv.style.display = "none";
            resultDiv.style.display = "flex";
            resultWords.innerText = scoreCounter + " Words"
        }
    }
});

newBtn.addEventListener('click', () => {   
    if (newWords.includes(randomWords)) {
        removeWord(newWords, randomWords)
        scoreCounter++;
        scoreText.innerText = "Score | " + scoreCounter;
        seenWords.push(randomWords);
        pickRandomWord();
    }  else {
        pickRandomWord();
        liveCounter--;
        livesText.innerText = "Lives | " + liveCounter;
        if (liveCounter == 0) {
            seenDiv.style.display = "none";
            resultDiv.style.display = "flex";
            resultWords.innerText = scoreCounter + " Words"
        }
    }
});

saveScoreBtn.addEventListener('click', () => {
    localStorage.setItem('verbalScore', scoreCounter + " Words " )
});